def _split_kinds_arg(value: str) -> List[str]:
    items = []
    for part in value.split(","):
        k = part.strip()
        if k:
            items.append(k)
    return items


def _prompt_yes_no(question: str, default_yes: bool = True) -> bool:
    suffix = "Y/n" if default_yes else "y/N"
    while True:
        ans = input(f"{question} ({suffix}): ").strip().lower()
        if not ans:
            return default_yes
        if ans in {"y", "yes"}:
            return True
        if ans in {"n", "no"}:
            return False
        print("请输入 y 或 n。")


def _update_dotenv(path: Path, key: str, value: str) -> None:
    lines: List[str] = []
    if path.exists():
        lines = path.read_text(encoding="utf-8").splitlines()

    out: List[str] = []
    replaced = False
    pattern = re.compile(rf"^\s*{re.escape(key)}\s*=")
    for line in lines:
        if pattern.match(line) and not replaced:
            out.append(f"{key}={value}")
            replaced = True
        else:
            out.append(line)

    if not replaced:
        if out and out[-1].strip() != "":
            out.append("")
        out.append(f"{key}={value}")

    path.write_text("\n".join(out) + "\n", encoding="utf-8")


def _choose_kinds_interactively() -> List[str]:
    keys = list(KINDS.keys())
    print("")
    print("请选择要生成的 Python 示例（可多选）：")
    for i, k in enumerate(keys, start=1):
        kind = KINDS[k]
        print(f"{i}. {kind.label} ({kind.key}) - {kind.description}")

    print("")
    print("输入示例：1,3,5  或  all")
    raw = input("请选择（默认 1）: ").strip().lower()
    if not raw:
        return [keys[0]]
    if raw == "all":
        return keys

    selected: List[str] = []
    for part in raw.split(","):
        part = part.strip()
        if not part:
            continue
        if part.isdigit():
            idx = int(part)
            if 1 <= idx <= len(keys):
                selected.append(keys[idx - 1])
            else:
                raise ValueError(f"无效序号: {part}")
        else:
            if part in KINDS:
                selected.append(part)
            else:
                raise ValueError(f"无效类型: {part}")

    # Deduplicate while preserving order.
    dedup: List[str] = []
    seen = set()
    for k in selected:
        if k not in seen:
            dedup.append(k)
            seen.add(k)
    return dedup


def generate(out_dir: Path, kinds: Iterable[str], model_id: str, base_url: str) -> List[Path]:
    out_dir.mkdir(parents=True, exist_ok=True)
    written: List[Path] = []
    for key in kinds:
        if key not in KINDS:
            raise ValueError(f"unknown kind: {key}")
        kind = KINDS[key]
        content = kind.renderer(model_id, base_url)
        path = out_dir / kind.filename
        path.write_text(content, encoding="utf-8")
        written.append(path)
    return written


def main(argv: List[str]) -> int:
    parser = argparse.ArgumentParser(description="Generate Ark Python example scripts.")
    parser.add_argument("--kinds", help="Comma-separated kinds (e.g. chat_basic,tools_web_search)")
    parser.add_argument("--out-dir", default=str(DEFAULT_OUT_DIR), help="Output directory for generated scripts")
    parser.add_argument("--model-id", default=DEFAULT_MODEL_ID, help="Default model id to embed in generated scripts")
    parser.add_argument("--base-url", default=DEFAULT_BASE_URL, help="Ark base_url for generated scripts")
    args = parser.parse_args(argv)

    out_dir = Path(args.out_dir).expanduser()
    model_id = (args.model_id or DEFAULT_MODEL_ID).strip()
    base_url = (args.base_url or DEFAULT_BASE_URL).strip()

    if args.kinds:
        kinds = _split_kinds_arg(args.kinds)
        written = generate(out_dir=out_dir, kinds=kinds, model_id=model_id, base_url=base_url)
        for p in written:
            print(str(p))
        return 0

    # Interactive mode
    print("==================================================")
    print("   Ark Python 示例脚本生成器")
    print("==================================================")
    print("")

    api_key = os.environ.get("ARK_API_KEY", "").strip()
    if not api_key:
        api_key = input("请输入 ARK_API_KEY（回车确认）: ").strip()
        if not api_key:
            print("ARK_API_KEY 不能为空。", file=sys.stderr)
            return 2
        os.environ["ARK_API_KEY"] = api_key

        dotenv_path = PROJECT_ROOT / ".env"
        if _prompt_yes_no("是否将 ARK_API_KEY 写入项目根目录 .env 以便后续复用？", default_yes=True):
            _update_dotenv(dotenv_path, "ARK_API_KEY", api_key)
            print(f"已写入: {dotenv_path}")
            print("提示：后续可用 'set -a; source .env; set +a' (macOS/Linux) 或 'setx ARK_API_KEY \"...\"' (Windows) 设置到环境变量。")
            print("")

    print(f"默认 Model ID: {DEFAULT_MODEL_ID}")
    model_id_input = input("请输入 Model ID（回车使用默认）: ").strip()
    if model_id_input:
        model_id = model_id_input

    out_dir_input = input(f"输出目录（回车使用默认 {DEFAULT_OUT_DIR}）: ").strip()
    if out_dir_input:
        out_dir = Path(out_dir_input).expanduser()

    try:
        kinds = _choose_kinds_interactively()
    except Exception as e:
        print(f"选择失败: {e}", file=sys.stderr)
        return 2

    written = generate(out_dir=out_dir, kinds=kinds, model_id=model_id, base_url=base_url)
    print("")
    print("生成完成：")
    for p in written:
        print(f"- {p}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))

