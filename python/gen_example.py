def _render_common_prelude(model_id: str, base_url: str) -> str:
    # Keep generated scripts self-contained and safe: no API key hardcoding.
    return f'''# -*- coding: utf-8 -*-
"""
This file is generated by python/gen_example.py

Notes:
- API Key: read from environment variable ARK_API_KEY
- Model ID: edit MODEL_ID below if needed
"""

from __future__ import annotations

import os
import sys
from pathlib import Path

from volcenginesdkarkruntime import Ark


MODEL_ID = "{model_id}"
BASE_URL = "{base_url}"


def _load_dotenv_if_present() -> None:
    """
    Best-effort .env loader for local runs.
    It only reads ARK_API_KEY and does NOT export globally.
    """
    # Generated scripts live at python/generated/*.py, so project root is parents[2].
    project_root = Path(__file__).resolve().parents[2]
    env_path = project_root / ".env"
    if not env_path.exists():
        return

    try:
        for raw_line in env_path.read_text(encoding="utf-8").splitlines():
            line = raw_line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            k, v = line.split("=", 1)
            k = k.strip()
            v = v.strip().strip('"').strip("'")
            if k == "ARK_API_KEY" and v and not os.environ.get("ARK_API_KEY"):
                os.environ["ARK_API_KEY"] = v
                return
    except Exception:
        # Ignore dotenv errors; fall back to direct env var.
        return


def _get_api_key() -> str:
    _load_dotenv_if_present()
    api_key = os.environ.get("ARK_API_KEY")
    if api_key:
        return api_key.strip()

    # Non-interactive mode should fail fast.
    if sys.stdin is None or not sys.stdin.isatty():
        raise RuntimeError("ARK_API_KEY 未设置，且当前为非交互模式。请先 export ARK_API_KEY 或写入项目根目录 .env。")

    api_key = input("请输入 ARK_API_KEY (回车确认): ").strip()
    if not api_key:
        raise RuntimeError("ARK_API_KEY 不能为空。")
    os.environ["ARK_API_KEY"] = api_key
    return api_key


def _new_client(api_key: str) -> Ark:
    # Keep base_url explicit to avoid confusion in different regions/environments.
    return Ark(api_key=api_key, base_url=BASE_URL)
'''


def _render_chat_basic(model_id: str, base_url: str) -> str:
    return (
        _render_common_prelude(model_id, base_url)
        + '''

def main() -> None:
    api_key = _get_api_key()
    client = _new_client(api_key)

    prompt = "请用简洁的语言解释什么是人工智能。"
    completion = client.chat.completions.create(
        model=MODEL_ID,
        messages=[
            {"role": "system", "content": "你是一个专业的开发助手。"},
            {"role": "user", "content": prompt},
        ],
    )

    print(completion.choices[0].message.content)


if __name__ == "__main__":
    main()
'''
    )


